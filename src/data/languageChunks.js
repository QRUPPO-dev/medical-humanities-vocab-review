let languageChunkIndex = 0;

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function resolveBlankWord(term, blankWord) {
  if (blankWord === 'medicines' && term.includes("medicine's")) return "medicine's";
  return blankWord;
}

function buildBlankPrompt(term, blankWord) {
  const answer = resolveBlankWord(term, blankWord);
  if (!answer) return term;

  const firstLetter = answer[0] ?? '';
  return term.replace(new RegExp(escapeRegExp(answer), 'i'), `${firstLetter}____`);
}

