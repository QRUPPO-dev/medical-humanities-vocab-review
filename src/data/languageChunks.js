function selectChunkStudyWord(chunk) {
  const word = chunk.blankWord?.trim() ?? '';
  const normalized = word.toLowerCase();

  if (!word || BASIC_STOP_WORDS.has(normalized) || LOW_VALUE_CHUNK_WORDS.has(normalized)) return '';
  if (word.length < 8 && !word.includes('-') && !SHORT_KEEP_WORDS.has(normalized)) return '';
  return word;
}

