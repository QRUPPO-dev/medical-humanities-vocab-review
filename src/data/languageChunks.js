export const LANGUAGE_CHUNK_WORD_ITEMS = Array.from(
  LANGUAGE_CHUNK_ITEMS.reduce((itemsByContext, chunk) => {
    const word = selectChunkStudyWord(chunk);
    if (!word) return itemsByContext;
    const fillWord = resolveBlankWord(chunk.term, word);

    const key = `${fillWord.toLowerCase()}-${chunk.id}`;
    if (itemsByContext.has(key)) return itemsByContext;

    itemsByContext.set(key, {
      id: `chunk-fill-${chunk.id}`,
      unit: chunk.unit,
      type: 'chunkWord',
      term: fillWord,
      meaning: chunk.meaning,
      blankPrompt: buildBlankPrompt(chunk.term, fillWord),
      blankInitial: fillWord[0] ?? '',
      example: chunk.term,
      exampleTranslation: chunk.meaning,
      source: chunk.source,
      tags: [`Unit ${chunk.unit}`, '填空词', 'Language Chunks'],
      sourceNote:
        chunk.sourceNote === '中文释义已按医学英语术语校正'
          ? '来自 Language Chunks 原题填空，中文提示已术语校正'
          : '来自 Language Chunks 原题填空',
    });

    return itemsByContext;
  }, new Map()).values(),
);

export const LANGUAGE_CHUNK_VOCAB_ITEMS = Array.from(
  Object.entries(CHUNK_PROFESSIONAL_WORD_MEANINGS).reduce((itemsByWord, [normalizedWord, meaning]) => {
    const sourceItem = LANGUAGE_CHUNK_WORD_ITEMS.find((item) => item.term.toLowerCase() === normalizedWord);
    if (!sourceItem || itemsByWord.has(normalizedWord)) return itemsByWord;

    itemsByWord.set(normalizedWord, {
      id: `chunk-vocab-${normalizedWord.replace(/[^a-z0-9]+/g, '-')}`,
      unit: sourceItem.unit,
      type: 'word',
      term: sourceItem.term,
      meaning,
      example: sourceItem.example,
      exampleTranslation: sourceItem.exampleTranslation,
      source: sourceItem.source,
      tags: [`Unit ${sourceItem.unit}`, '专业词', 'Language Chunks'],
      sourceNote: '从 Language Chunks 填空词筛选出的专业词',
    });

    return itemsByWord;
  }, new Map()).values(),
);
